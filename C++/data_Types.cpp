#include <iostream>
using namespace std;
int main(){
    int a;
    a=12;
    cout<<"size of int"<<sizeof(a)<<endl;
    char b;
    cout<<"size of char"<<sizeof(b)<<endl;
    float c;
    cout<<"size of float"<<sizeof(c)<<endl;
    bool d;
    cout<<"size of bool"<<sizeof(d)<<endl;
    short int si;
    long int li;
    cout<<"size of shortint"<<sizeof(si)<<endl;
    cout<<"size of longint"<<sizeof(li)<<endl;
return 0;
}
